document.addEventListener('DOMContentLoaded', function () {
    const langSwitcher = document.getElementById('lang-switcher');

    if (langSwitcher) {
        langSwitcher.addEventListener('click', function () {
            const languages = ['uk', 'en', 'lt', 'ru'];
            let currentLang = this.getAttribute('data-lang') || 'uk';
            let nextLangIndex = (languages.indexOf(currentLang) + 1) % languages.length;
            let nextLang = languages[nextLangIndex];

            console.log('Current Language:', currentLang);
            console.log('Next Language:', nextLang);

            // Перевіряємо, чи є переклади для наступної мови
            if (!translations[nextLang]) {
                console.error(`Translation for language "${nextLang}" not found.`);
                return; // Завершуємо, якщо переклад відсутній
            }

            // Оновлюємо текст кнопки
            this.textContent = nextLang.toUpperCase();
            this.setAttribute('data-lang', nextLang);

            // Змінюємо текст на сторінці
            document.querySelectorAll('[data-lang]').forEach(element => {
                const key = element.getAttribute('data-lang');
                if (translations[nextLang][key]) {
                    element.textContent = translations[nextLang][key];
                } else {
                    console.warn(`Key "${key}" not found in translations for language "${nextLang}".`);
                }
            });

            // Оновлюємо специфічні елементи
            const heroTitle = translations[nextLang]["hero-title"];
            const heroLead = translations[nextLang]["hero-lead"];
            const aboutTitle = translations[nextLang]["about-title"];
            const aboutDescription = translations[nextLang]["about-description"];

            if (heroTitle) document.querySelector('.hero h1').textContent = heroTitle;
            if (heroLead) document.querySelector('.hero p').textContent = heroLead;
            if (aboutTitle) document.querySelector('.about h2').textContent = aboutTitle;
            if (aboutDescription) document.querySelector('.about .company-description').textContent = aboutDescription;
        });
    } else {
        console.error('Елемент з ID "lang-switcher" не знайдено в DOM.');
    }
});

